import {AppConsts} from '@shared/AppConsts';
import { Component, Injector, ViewEncapsulation, ViewChild } from '@angular/core';
import { ActivatedRoute , Router} from '@angular/router';
import { OrderTaskMapsServiceProxy, OrderTaskMapDto  } from '@shared/service-proxies/service-proxies';
import { NotifyService } from 'abp-ng2-module';
import { AppComponentBase } from '@shared/common/app-component-base';
import { TokenAuthServiceProxy } from '@shared/service-proxies/service-proxies';
import { CreateOrEditOrderTaskMapModalComponent } from './create-or-edit-orderTaskMap-modal.component';

import { ViewOrderTaskMapModalComponent } from './view-orderTaskMap-modal.component';
import { appModuleAnimation } from '@shared/animations/routerTransition';
import { Table } from 'primeng/table';
import { Paginator } from 'primeng/paginator';
import { LazyLoadEvent } from 'primeng/api';
import { FileDownloadService } from '@shared/utils/file-download.service';
import { filter as _filter } from 'lodash-es';
import { DateTime } from 'luxon';

             import { DateTimeService } from '@app/shared/common/timing/date-time.service';

@Component({
    templateUrl: './orderTaskMaps.component.html',
    encapsulation: ViewEncapsulation.None,
    animations: [appModuleAnimation()]
})
export class OrderTaskMapsComponent extends AppComponentBase {
    
    
    @ViewChild('createOrEditOrderTaskMapModal', { static: true }) createOrEditOrderTaskMapModal: CreateOrEditOrderTaskMapModalComponent;
    @ViewChild('viewOrderTaskMapModal', { static: true }) viewOrderTaskMapModal: ViewOrderTaskMapModalComponent;   
    
    @ViewChild('dataTable', { static: true }) dataTable: Table;
    @ViewChild('paginator', { static: true }) paginator: Paginator;

    advancedFiltersAreShown = false;
    filterText = '';
        orderInvoiceNumberFilter = '';
        taskEventNameFilter = '';






    constructor(
        injector: Injector,
        private _orderTaskMapsServiceProxy: OrderTaskMapsServiceProxy,
        private _notifyService: NotifyService,
        private _tokenAuth: TokenAuthServiceProxy,
        private _activatedRoute: ActivatedRoute,
        private _fileDownloadService: FileDownloadService,
             private _dateTimeService: DateTimeService
    ) {
        super(injector);
    }

    getOrderTaskMaps(event?: LazyLoadEvent) {
        if (this.primengTableHelper.shouldResetPaging(event)) {
            this.paginator.changePage(0);
            if (this.primengTableHelper.records &&
                this.primengTableHelper.records.length > 0) {
                return;
            }
        }

        this.primengTableHelper.showLoadingIndicator();

        this._orderTaskMapsServiceProxy.getAll(
            this.filterText,
            this.orderInvoiceNumberFilter,
            this.taskEventNameFilter,
            this.primengTableHelper.getSorting(this.dataTable),
            this.primengTableHelper.getSkipCount(this.paginator, event),
            this.primengTableHelper.getMaxResultCount(this.paginator, event)
        ).subscribe(result => {
            this.primengTableHelper.totalRecordsCount = result.totalCount;
            this.primengTableHelper.records = result.items;
            this.primengTableHelper.hideLoadingIndicator();
        });
    }

    reloadPage(): void {
        this.paginator.changePage(this.paginator.getPage());
    }

    createOrderTaskMap(): void {
        this.createOrEditOrderTaskMapModal.show();        
    }


    deleteOrderTaskMap(orderTaskMap: OrderTaskMapDto): void {
        this.message.confirm(
            '',
            this.l('AreYouSure'),
            (isConfirmed) => {
                if (isConfirmed) {
                    this._orderTaskMapsServiceProxy.delete(orderTaskMap.id)
                        .subscribe(() => {
                            this.reloadPage();
                            this.notify.success(this.l('SuccessfullyDeleted'));
                        });
                }
            }
        );
    }

    exportToExcel(): void {
        this._orderTaskMapsServiceProxy.getOrderTaskMapsToExcel(
        this.filterText,
            this.orderInvoiceNumberFilter,
            this.taskEventNameFilter,
        )
        .subscribe(result => {
            this._fileDownloadService.downloadTempFile(result);
         });
    }
    
    
    
    
    

    resetFilters(): void {
        this.filterText = '';
        		this.orderInvoiceNumberFilter = '';
							this.taskEventNameFilter = '';
					
        this.getOrderTaskMaps();
    }
}
